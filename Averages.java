public class Averages
{
	public static void main(String[] args)
	{
		int x = 5, y = 8, z = 4;
		double result;
		
		result = (x + y + z) / 3.0;
		System.out.println("Average of " + x + ", " + y + " and " + z + " is " + result);
	}
}//Computes the average between three numbers x, y and z.